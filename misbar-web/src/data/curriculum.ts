// Helper function to get content for a specific grade, subject, and semester
export function getContent(grade: string, subject: string, semester: string): Unit[] {
  const gradeData = curriculumData[grade];
  if (!gradeData) return [];
  const subjectData = gradeData[subject];
  if (!subjectData) return [];
  return semester === 'الأول' ? subjectData.semester1 : subjectData.semester2;
}

// Helper to get NAFS standards for a specific week
export function getNafsStandards(grade: string, subject: string, week: string): string[] {
  const gradeStandards = nafsWeeklyStandards[grade];
  if (!gradeStandards) return [];
  const subjectStandards = gradeStandards[subject];
  if (!subjectStandards) return [];
  return subjectStandards[week] || [];
}
